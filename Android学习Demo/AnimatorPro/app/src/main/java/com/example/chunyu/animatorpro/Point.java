public class Point {

    private float x;

    public Point(float x, float y) {
        this.x = x;
        this.y = y;
    }

    private float y;

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public float getX() {

        return x;
    }

    public void setX(float x) {
        this.x = x;
    }
}
